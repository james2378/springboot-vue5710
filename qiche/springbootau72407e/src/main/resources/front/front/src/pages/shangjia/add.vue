<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            shangjiahao : false,
            mima : false,
            shangjiamingcheng : false,
            shangjiadizhi : false,
            lianxidianhua : false,
            shangjiayouxiang : false,
            yingyezhizhao : false,
            shangpuzhaopian : false,
            shangjiajieshao : false,
            money : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          shangjiahao: '',
          mima: '',
          shangjiamingcheng: '',
          shangjiadizhi: '',
          lianxidianhua: '',
          shangjiayouxiang: '',
          yingyezhizhao: '',
          shangpuzhaopian: '',
          shangjiajieshao: '',
          money: '',
        },


        rules: {
          shangjiahao: [
            { required: true, message: '商家号不能为空', trigger: 'blur' },
          ],
          mima: [
            { required: true, message: '密码不能为空', trigger: 'blur' },
          ],
          shangjiamingcheng: [
            { required: true, message: '商家名称不能为空', trigger: 'blur' },
          ],
          shangjiadizhi: [
          ],
          lianxidianhua: [
            { validator: this.$validate.isMobile, trigger: 'blur' },
          ],
          shangjiayouxiang: [
            { validator: this.$validate.isEmail, trigger: 'blur' },
          ],
          yingyezhizhao: [
          ],
          shangpuzhaopian: [
          ],
          shangjiajieshao: [
          ],
          money: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='shangjiahao'){
              this.ruleForm.shangjiahao = obj[o];
              this.ro.shangjiahao = true;
              continue;
            }
            if(o=='mima'){
              this.ruleForm.mima = obj[o];
              this.ro.mima = true;
              continue;
            }
            if(o=='shangjiamingcheng'){
              this.ruleForm.shangjiamingcheng = obj[o];
              this.ro.shangjiamingcheng = true;
              continue;
            }
            if(o=='shangjiadizhi'){
              this.ruleForm.shangjiadizhi = obj[o];
              this.ro.shangjiadizhi = true;
              continue;
            }
            if(o=='lianxidianhua'){
              this.ruleForm.lianxidianhua = obj[o];
              this.ro.lianxidianhua = true;
              continue;
            }
            if(o=='shangjiayouxiang'){
              this.ruleForm.shangjiayouxiang = obj[o];
              this.ro.shangjiayouxiang = true;
              continue;
            }
            if(o=='yingyezhizhao'){
              this.ruleForm.yingyezhizhao = obj[o];
              this.ro.yingyezhizhao = true;
              continue;
            }
            if(o=='shangpuzhaopian'){
              this.ruleForm.shangpuzhaopian = obj[o].split(",")[0];
              this.ro.shangpuzhaopian = true;
              continue;
            }
            if(o=='shangjiajieshao'){
              this.ruleForm.shangjiajieshao = obj[o];
              this.ro.shangjiajieshao = true;
              continue;
            }
            if(o=='money'){
              this.ruleForm.money = obj[o];
              this.ro.money = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`shangjia/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('shangjia/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post(`shangjia/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post(`shangjia/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      yingyezhizhaoUploadChange(fileUrls) {
          this.ruleForm.yingyezhizhao = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
      shangpuzhaopianUploadChange(fileUrls) {
          this.ruleForm.shangpuzhaopian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

