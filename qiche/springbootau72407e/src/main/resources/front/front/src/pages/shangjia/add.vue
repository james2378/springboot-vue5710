                placeholder="密码" clearable :readonly=" false  ||ro.mima"></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"none"}' label="商家名称" prop="shangjiamingcheng">
            <el-input  v-model="ruleForm.shangjiamingcheng" 
                placeholder="商家名称" clearable :readonly=" false  ||ro.shangjiamingcheng"></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"none"}' label="商家地址" prop="shangjiadizhi">
            <el-input  v-model="ruleForm.shangjiadizhi" 
                placeholder="商家地址" clearable :readonly=" false  ||ro.shangjiadizhi"></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"none"}' label="联系电话" prop="lianxidianhua">
            <el-input  v-model="ruleForm.lianxidianhua" 
                placeholder="联系电话" clearable :readonly=" false  ||ro.lianxidianhua"></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"none"}' label="商家邮箱" prop="shangjiayouxiang">
            <el-input  v-model="ruleForm.shangjiayouxiang" 
                placeholder="商家邮箱" clearable :readonly=" false  ||ro.shangjiayouxiang"></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"none"}' label="营业执照" prop="yingyezhizhao">
            <file-upload
            tip="点击上传营业执照"
            action="file/upload"
            :limit="1"
