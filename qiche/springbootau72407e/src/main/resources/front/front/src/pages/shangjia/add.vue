            :fileUrls="ruleForm.yingyezhizhao?ruleForm.yingyezhizhao:''"
            @change="yingyezhizhaoUploadChange"
            ></file-upload>
          </el-form-item>  
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px","background":"none"}' label="商铺照片" v-if="type!='cross' || (type=='cross' && !ro.shangpuzhaopian)" prop="shangpuzhaopian">
            <file-upload
            tip="点击上传商铺照片"
            action="file/upload"
